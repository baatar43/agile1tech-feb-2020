package basicJava;

public class numberOperation {

	public static void main(String[] args) {
		
		int sum; 
		int x = 15; 
		
		System.out.println(x); 
		
		int y = 100; 
		
		x = x+y; 
		//x=115
		sum=x+y; 
	// sum=215
		int sub; 
		// 215 - 115 
		sub = sum-x;
		
		System.out.println(x); 
		
		System.out.println(sum); 
		
		System.out.println(sub); 

	}

}
